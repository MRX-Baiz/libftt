#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	C;
	int	k;

	C = (char)c;
	k = ft_strlen(s);
	while (k >= 0)
	{
		if(s[k] == C)
			return ((char *)&s[k]);
		k--;
	}
	return (NULL);
}
